namespace Rehawk.UIFramework
{
    /// <summary>
    /// Represents the base class for view models used in the UI framework.
    /// </summary>
    /// <remarks>
    /// This abstract class serves as a foundation for implementing view models.
    /// It inherits from <see cref="BindableMonoBehaviourBase"/>, enabling property change notification and facilitating data binding.
    /// </remarks>
    public abstract class ViewModelBase : BindableMonoBehaviourBase {}
}